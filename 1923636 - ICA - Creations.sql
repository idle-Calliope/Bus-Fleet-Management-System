/*---------TABLES-----------*/
CREATE TABLE BUS_FLEET(
	BUS_FLEET_CODE VARCHAR(50) NOT NULL,
	BUS_FLEET_NAME VARCHAR(MAX),
	NO_OF_BUSES INT

	PRIMARY KEY (BUS_FLEET_CODE)
);

CREATE TABLE EMPLOYEES(
	NATIONAL_ID INT PRIMARY KEY NOT NULL,
	BUS_F_CODE VARCHAR(50) NOT NULL,
	FIRST_NAME VARCHAR(MAX),
	LAST_NAME VARCHAR(MAX),
	PHONE_NO INT,
	DISTRICT VARCHAR(MAX),
	VILLAGE VARCHAR(MAX),
	STREET VARCHAR(MAX),
	AGE INT,
	SEX VARCHAR(MAX),
	SALARY MONEY,
	ROLES VARCHAR(MAX)
	
	FOREIGN KEY (BUS_F_CODE) REFERENCES BUS_FLEET (BUS_FLEET_CODE)
);

CREATE TABLE SECRETARIES(
	SECRETARY_ID INT PRIMARY KEY NOT NULL,
	N_ID INT NOT NULL,
	ROLES_ VARCHAR(MAX),

	FOREIGN KEY (N_ID) REFERENCES EMPLOYEES (NATIONAL_ID)
);

CREATE TABLE MECHANICS(
	MECHANIC_ID INT PRIMARY KEY NOT NULL,
	_ID INT NOT NULL,
	TYPE_OF_MECHANICS VARCHAR(MAX)

	FOREIGN KEY (_ID) REFERENCES EMPLOYEES (NATIONAL_ID)
);

CREATE TABLE RANK_INSPECTOR(
	RANK_INSPECTOR_ID INT PRIMARY KEY NOT NULL,
	NAT_ID INT NOT NULL,
	SHIFT_ VARCHAR(MAX),
	TIME_IN TIME,
	TIME_OUT TIME,

	FOREIGN KEY (NAT_ID) REFERENCES EMPLOYEES (NATIONAL_ID)
);

CREATE TABLE RANK_AUTHORITIES(
	RANK_AUTHORITY_ID INT PRIMARY KEY NOT NULL,
	NL_ID INT NOT NULL,
	POSITION VARCHAR(50)

	FOREIGN KEY (NL_ID) REFERENCES EMPLOYEES (NATIONAL_ID)
);

CREATE TABLE ASSISTANT(
	ASSISTANT_ID INT PRIMARY KEY NOT NULL,
	NAL_ID INT NOT NULL,
	RANK_AUTH_ID INT NOT NULL,

	FOREIGN KEY (NAL_ID) REFERENCES EMPLOYEES (NATIONAL_ID),
	FOREIGN KEY (RANK_AUTH_ID) REFERENCES RANK_AUTHORITIES (RANK_AUTHORITY_ID)
);

CREATE TABLE ATTENDEE(
	ATTENDEE_ID INT PRIMARY KEY NOT NULL,
	RANK_AUT_ID INT NOT NULL,
	NAAL_ID INT NOT NULL,

	FOREIGN KEY (NAAL_ID) REFERENCES EMPLOYEES (NATIONAL_ID),
	FOREIGN KEY (RANK_AUT_ID) REFERENCES RANK_AUTHORITIES (RANK_AUTHORITY_ID)
);

CREATE TABLE MANAGER(
	MANAGER_ID INT PRIMARY KEY NOT NULL,
	RANK_AU_ID INT NOT NULL,
	NATAL_ID INT NOT NULL,

	FOREIGN KEY (NATAL_ID) REFERENCES EMPLOYEES (NATIONAL_ID),
	FOREIGN KEY (RANK_AU_ID) REFERENCES RANK_AUTHORITIES (RANK_AUTHORITY_ID)
);

CREATE TABLE DRIVER(
	DRIVER_ID INT PRIMARY KEY NOT NULL,
	RANK_A_ID INT NOT NULL,
	NATI_ID INT NOT NULL,

	FOREIGN KEY (NATI_ID) REFERENCES EMPLOYEES (NATIONAL_ID),
	FOREIGN KEY (RANK_A_ID) REFERENCES RANK_AUTHORITIES (RANK_AUTHORITY_ID)
);

CREATE TABLE BUSES(
	BUS_CODE VARCHAR(50) NOT NULL,
	BUS_FT_CODE VARCHAR(50)UNIQUE NOT NULL,
	CLASS VARCHAR(MAX),
	TYPE_ VARCHAR(MAX),
	ARRIVAL_TIME TIME,
	DEPARTURE_TIME TIME,
	DURATION VARCHAR(MAX),
	SOURCE_ VARCHAR(MAX),
	DESTINATION VARCHAR(MAX),
	
	CONSTRAINT BUS_CODE PRIMARY KEY (BUS_CODE),
	FOREIGN KEY (BUS_FT_CODE) REFERENCES BUS_FLEET (BUS_FLEET_CODE)
);

CREATE TABLE BUS_RANKS(
	RANK_NAME VARCHAR(50) NOT NULL,
	BUS_CODE VARCHAR (50) NOT NULL,
	BUS_F__CODE VARCHAR(50)NOT NULL,
	NO_OF_BUSES_LEFT INT,
	NO_OF_BUSES_ARRIVED INT,
	WORKING_EMPLOYEE INT,

	CONSTRAINT RANK_NAME PRIMARY KEY (RANK_NAME),
	FOREIGN KEY (BUS_F__CODE) REFERENCES BUSES (BUS_FT_CODE),
	FOREIGN KEY (BUS_CODE) REFERENCES BUSES (BUS_CODE)

);

CREATE TABLE CUSTOMERS(
	CUSTOMER_ID INT PRIMARY KEY NOT NULL,
	CUSTOMER_FIRST_NAME VARCHAR(MAX),
	CUSTOMER_LAST_NAME VARCHAR(MAX),
	DISTRICT VARCHAR(MAX),
	VILLAGE VARCHAR(MAX),
	STREET VARCHAR(MAX),
	POSTAL_ADDRESS VARCHAR(MAX),
	PHONE_NO INT

);

CREATE TABLE PARCELS(
	PARCEL_NO INT PRIMARY KEY NOT NULL,
	ITEM VARCHAR(50),
	QUANTITY INT,
	CUSTOMER_ID INT NOT NULL,
	BUS_CODE VARCHAR(50) NOT NULL,
	BUS_FLEET_CODE VARCHAR(50) NOT NULL,
	WEIGHT_ FLOAT,
	SOURCE_ VARCHAR(MAX),
	DESTINATION VARCHAR(MAX),
	FARE MONEY,
	TIME_STAMP TIME,

	FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
	FOREIGN KEY (BUS_FLEET_CODE) REFERENCES BUSES(BUS_FT_CODE),
	FOREIGN KEY (BUS_CODE) REFERENCES BUSES (BUS_CODE)
);

CREATE TABLE PASSENGERS(
	PASSENGER_ID INT IDENTITY (1,1) PRIMARY KEY NOT NULL,
	PASSPORT_NO VARCHAR(50) UNIQUE NOT NULL,
	BUS_C VARCHAR(50) NOT NULL,
	BUS_FLEET_CODE VARCHAR(50) NOT NULL,
	FIRST_NAME VARCHAR(MAX),
	LAST_NAME VARCHAR(MAX),
	DISTRICT VARCHAR(MAX),
	VILLAGE VARCHAR(MAX),
	STREET VARCHAR(MAX),
	AGE INT,
	SEX VARCHAR(MAX),
	PHONE_NO INT,

	FOREIGN KEY (BUS_FLEET_CODE) REFERENCES BUSES(BUS_FT_CODE),
	FOREIGN KEY (BUS_C) REFERENCES BUSES (BUS_CODE)
);

CREATE TABLE RESERVATIONS(
	BOOKING_ID INT PRIMARY KEY NOT NULL,
	BUS_CODE VARCHAR(50) NOT NULL,
	BUS_FLEET_CODE VARCHAR(50) NOT NULL,
	BOOKING_DATE DATE,
	NO_OF_TICKETS INT,

	FOREIGN KEY (BUS_FLEET_CODE) REFERENCES BUSES(BUS_FT_CODE),
	FOREIGN KEY(BUS_CODE) REFERENCES BUSES(BUS_CODE)
);

CREATE TABLE TICKETS(
	TICKET_NO INT  NOT NULL,
	BUS_FLEE_CODE VARCHAR(50) NOT NULL,
	BUS_CODE VARCHAR(50) NOT NULL,
	BOOKING INT NOT NULL,
	BUS_FLEET_NAME VARCHAR(MAX),
	PASSENGERS_ID INT,
	DATE_ DATE,
	SEAT_NO INT,
	CLASS1 VARCHAR(MAX),
	TYPE_2 VARCHAR(MAX),
	FARE MONEY,

	CONSTRAINT TICKET_NO PRIMARY KEY (BUS_FLEE_CODE, BUS_CODE, TICKET_NO),
	FOREIGN KEY (BOOKING) REFERENCES RESERVATIONS (BOOKING_ID),
	FOREIGN KEY (PASSENGERS_ID) REFERENCES PASSENGERS(PASSENGER_ID)
);

